<p>&hellip;Софи и Тьерри счастливы, что родились от такого большого удовольствия. Но кое-что еще удивляет: папа им объяснил, как рождается один малыш, а вот о близнецах, как они, он ничего не рассказал.</p>
<p>&mdash; Хорошо,&mdash; говорит папа,&mdash; я объясню. Просто у мамы в животике была не одна оплодотворенная яйцеклетка, а две, которые вышли из яичников в одно и то же время. Каждая яйцеклетка встретила свой сперматозоид, и образовались два плода. Случается иногда, правда значительно реже, что не две, а больше яйцеклеток оплодотворяются, тогда образуется много плодов. Но так как их слишком много, им не хватает места для нормального роста, и детки рождаются очень маленькими и слабыми. Тем не менее есть семьи, где родителям удалось вырастить пятерых детей, рожденных одновременно.</p>
<p>Когда рождаются близнецы, роды протекают дольше и намного труднее. Но бывают разные осложнения и при рождении одного малыша. Например, когда ребенок выходит не головкой, а попкой.</p>

