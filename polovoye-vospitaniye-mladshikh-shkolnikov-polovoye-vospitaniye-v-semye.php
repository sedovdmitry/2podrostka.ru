</center>

<p>Дети были явно удивлены, но вопросов больше не задавали. Софи занялась со своей куклой, а Тьерри стал играть с собакой тети Николь. Женщины о чем-то болтали, мужчины смотрели футбольный матч по телевизору. Потом пили чай и все весело обсуждали событие, которое должно скоро произойти в семье,&mdash; появление на свет малыша.</p>
<p>Время бежит быстро. Уже 6 часов, пора домой. На обратном пути Софи и Тьерри молчали. Придя домой, они заперлись в своей комнате, но через некоторое время все же обратились к маме с вопросом, мучавшим их все это время:</p>
<p>&mdash; Ты сказала, что ребенок у тети Николь в животике. Как же так получилось, мы никак не можем понять!</p>
<p>Подошел папа, усадил ребят перед собой и начал рассказ:</p>
<p>&mdash; Это очень долгая история. Знаете ли вы, как познакомились тетя Николь и дядя Жак? Они несколько лет учились вместе в институте, привыкли вместе работать и готовиться к экзаменам.</p>
<p>Вечером они ходили в кино, в театр, на концерты. Им вместе было очень хорошо, они часто говорили, спорили, смеялись, им не хотелось расставаться. Сдав последние экзамены, они отправились на каникулы с друзьями. Веселая была компания. Они купались, играли в волейбол, загорали, вечерами устраивали танцы. Николь и Жак почти всегда танцевали вместе. Они полюбили друг друга. Им очень хотелось поцеловаться. Так началась их любовь.</p>

