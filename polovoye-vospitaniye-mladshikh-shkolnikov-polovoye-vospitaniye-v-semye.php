<p>Он хочет есть, но не знает, как это делается! Мама его накормит молоком, которое появляется сразу же после родов в ее груди. Если она хочет, то может дать ему молока из бутылочки. Кормить из бутылочки может и папа. Очень много забот у мамы и папы в первые недели и месяцы жизни малыша. Они должны его взвешивать, измерять рост, чтобы быть уверенными в том, что ребенок растет и поправляется.</p>

<center><img width="80%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image013.jpg" alt="Женщина кормит грудью младенца">
</center>

<p>Когда малыш пачкает пеленки, его нужно мыть и переодевать во все чистое. Это большая работа, но и удовольствие! Благодаря этому малышу родители чувствуют себя еще ближе и любят друг друга сильнее.</p>
<p>Малыш знает, что его любят, и находит, что жизнь замечательна. Он следит за родителями глазами, улыбается им. Очень скоро он замечает, что есть ручки, рассматривая их, он очень забавно лопочет. Ему нравится быть голеньким, дрыгать ножками и играть с ними. Через некоторое время мама просит его делать свои делишки на горшок. Это не очень нравится малышу, но, чтобы сделать приятное маме, он подчиняется. Теперь он чистенький и не пачкает больше пеленки.</p>

