<p align="center"><font size="5">РЕЦЕНЗИИ</p></font>
<p align="center"><font size="4">лауреаты Нобелевской премии по медицине Андре Львофф и Жан Моно</p></font>
<blockquote>Простота и ясность изложения ничуть не умаляют научного достоинства книги. Текст замечательно иллюстрирован, причем подбор цветов придает реальным вещам как бы оттенок нереальности. Мы не знаем другой книги, посвященной такой сложной и деликатной теме <b>полового воспитания в семье</b>, которая была бы такой удачной.</blockquote>

<p align="center"><font size="4">Леволиберальная ежедневная вечерняя газета &laquo;Le Monde&raquo; (&laquo;Мир&raquo;)</p></font>
<blockquote>Опыт показывает, что многие родители сами получают много информации из изданий, предназначенных даже для самых маленьких&hellip; Эта книга должна принести неоценимую пользу и преподавателям данного предмета. Шедевр ясности, прямоты, педагогики и такта. Замечательные иллюстрации: сочетание эстампов, фотографий, схем, рисунков&hellip; Бесспорно самая лучшая книга на тему <b>полового воспитания младших школьников</b>.</blockquote>

<p align="center"><font size="4">Еженедельный журнал правого толка &laquo;L'Express&raquo;</p></font>
<blockquote>Оригинальность книги в том, что она обращена и к детям и к взрослым. Научная строгость не исключает психологической и даже психоаналитической информации. Детям говорят даже о либидо, об эдиповых отношениях. Авторы порывают с пуританской традицией, согласно которой всякая плоть греховна, а <b>половая жизнь</b> допустима лишь для продолжения рода. Наконец, девичья невинность, общественное табу, получает беспристрастную оценку. Это может шокировать обывателей, но существенно расширяет наши представления.</blockquote>

<p align="center"><font size="4">Ежедневная коммунистическая газета &laquo;L’Humanité&raquo; (&laquo;Человечество&raquo;)</p></font>

<blockquote>Впервые во Франции осуществлена такая работа в тот момент, когда вопреки страхам некоторых родителей и сопротивлению правительства никто уже не отрицает необходимость <b>сексуальной информации как в семье, так и в школе</b>. Одним словом, перед нами результат решительного переосмысления традиционных ценностей.</blockquote>
<br />
<p align="center"><font size="5">Приступим к увлекательному и полезному чтению</p></font>

<p>Однажды в воскресенье после обеда вся семья: папа, мама, близнецы Софи и Тьерри, 7 лет, Франсуа, старший брат, 14 лет, Мартина, 12 лет, и пес Уранус отправились в гости к тетушке Николь.</p>
