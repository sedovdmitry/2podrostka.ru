<p>Или когда ребенок идет головкой, но он очень крупный, а мама худенькая, с узкими бедрами. Тогда акушерка или врач должны помочь головке выйти с помощью больших специальных щипцов. Еще бывают случаи, когда маму усыпляют и делают ей операцию, чтобы достать ребенка. Эта операция называется &laquo;кесарево сечение&raquo;. Она не очень опасна и проходит быстро. И, наконец, некоторые дети рождаются раньше срока, в 7 или 8 месяцев. Эти малыши еще очень слабенькие, не подготовленные к самостоятельной жизни. Их обязательно нужно поместить в специальное гнездышко, которое называется инкубатором. Там им тепло, как у мамы в животе, они надежно защищены от микробов и через некоторое время догонят малышей, которые родились вовремя.</p>
<p>Тьерри, слушая папу, ласкал своего пса, лежавшего у его ног. И неожиданно спросил:</p>
<p>&mdash; А как зверюшки, собаки например? Они так же делают своих деток, как и люди?</p>
<p>Софи засмеялась.</p>
<p>&mdash; Собаки? Я не знаю. А вот у птичек, во всяком случае, все по-другому. Ты же знаешь, что они откладывают яички.</p>
<p>&mdash; А наш Уран? Его мама &mdash; собака дяди Жака. Как он родился?</p>
<p>&mdash; Эта собака-мама встретила другую собаку, собаку-папу, и они соединились так, как показано на картинке.</p>

