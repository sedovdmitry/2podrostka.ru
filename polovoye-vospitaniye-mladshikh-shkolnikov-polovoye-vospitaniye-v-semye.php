<p>Сперматозоиды собаки-папы вошли во влагалище собаки-мамы. У нее внутри, как это почти всегда бывает у наших четвероногих друзей, было несколько созревших яйцеклеток.</p>
<p>Образовалось несколько плодов, прошло несколько недель, и на свет появился Уран со своими братьями и сестрами.</p>
<p>У их мамы много сосков, и молока хватило на всех, а когда Уран смог есть сам, мы взяли его к себе.</p>
<p>Теперь о птичках. Ты говоришь, что у них все происходит по-другому. На первый взгляд так и кажется. Но если бы мы могли заглянуть внутрь кенара и канарейки, как заглядываем внутрь часов, чтобы поглядеть на часовой механизм, мы увидели бы, что у птиц тоже есть половые органы. У кенара есть яички и маленький член, благодаря которому сперматозоиды попадают в брюшко к маме-канарейке. А у нее есть яичники.</p>
<p>Сперматозоиды кенара соединяются с яйцеклетками самочки. Многие из этих яйцеклеток уже созрели, и из них получается много яиц. Но прежде чем птенчики в яйцах окончательно сформируются, канарейка сносит яйцо.</p>
<p>Яйца защищены скорлупой, а чтобы они все время были в тепле, самочка прикрывает их своим телом; про это как раз и говорят: она сидит на яйцах.</p>

