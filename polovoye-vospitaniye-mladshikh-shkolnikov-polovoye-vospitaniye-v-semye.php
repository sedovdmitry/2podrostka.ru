<p>Когда пыльца попадает на пестик, ее частички сливаются с яйцеклетками, и от этого образуются семена. У фруктов семена называются косточками. Каждое семя обрастает плодом, это и есть &laquo;ребенок&raquo;, которого рождает растение. У некоторых фруктов внутри не одна косточка, а много.</p>
<p>Садовник бросает семя в землю. А если садовника рядом нет, семена разносит по свету ветер. Проходит несколько дней или несколько недель, семя в земле набухает, пускает росток, он пробивается из земли и тянется к солнцу. Проходит еще какое-то время, новое растение зацветает, и все начинается сначала.</p>
<p>&mdash; Да,&mdash; радостно восклицает Софи,&mdash; а главное, что мужчины и женщины любят друг друга и любят своих детей.</p>
