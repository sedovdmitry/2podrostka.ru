</center>

<p>Франсуа с удовольствием пошел бы в кино, но раз идут все&hellip; Мартина была очень довольна и горда, ведь мама разрешила ей самой выбирать подарки для тети Николь. Да не просто подарки! Близняшки смотрели на тетушку, с волнением развязывающую пакеты. А там крошечные рубашонки, чепчики и вязанные мамой и Мартиной кофточки. Поблагодарив их, Николь показывает прекрасную бело-голубую колыбельку.</p>

