<p>В один прекрасный день они совсем близки &mdash; и испытывают новое наслаждение. Это называется половым наслаждением. В момент наибольшей нежности сперматозоиды покидают член мужчины. Они входят во влагалище женщины, доходят до малой трубы, и порой сперматозоид встречает яйцеклетку. Вот таким образом, если они этого хотят, можно зачать ребенка.</p>

