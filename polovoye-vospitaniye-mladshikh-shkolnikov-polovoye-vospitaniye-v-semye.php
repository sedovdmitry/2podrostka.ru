<p>Проходит несколько лет. Родители решили, что малышу пора идти в детский сад. В первый день очень грустно расставаться. Но очень быстро он привыкает к другим детям. Воспитательница учит их рисовать, различать буквы. Во время переменки она организует игры, поет песенки, разговаривает с каждым из своих воспитанников.</p>

