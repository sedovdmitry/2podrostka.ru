<p>Тьерри еще маленький, у него пока еще нет волосиков, но у него уже, как у всех мужчин, как у вашего папы, есть мешочек кожи, в котором два шарика, названные яичками, и своего рода трубочка &mdash; член. Яички и член &mdash; <b>это еще не все половые органы</b>. Так же как и стрелочки в часах, это только часть часового механизма. Что же мы увидим, если заглянем в организм Тьерри? Мы увидим два тонких изогнутых канала, которые берут начало от двух шариков. Эти два канала соединяются немножко выше в животе и впадают в другой канал, откуда и <b>поступает моча в мужской половой член</b>.</p>
<p>Моча &mdash; это единственная жидкость, которая идет из члена маленького мальчика. Но из члена взрослого мужчины иногда выходит и другая жидкость. Она исходит из яичек. Если вы рассмотрите капельку этой жидкости под микроскопом, вы увидите движение множества &laquo;головастиков&raquo; с большой головкой и длинным подвижным хвостиком. Эти крохотные частички называются сперматозоиды (какое трудное слово!). <b>Именно они и необходимы для того, чтобы делать детей</b>.</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image024.jpg" alt="Рисунок. Мужские половые органы. Мочевой пузырь, семенник, яички, мужской половой член, сперматозоид">
</center>
<p>А теперь посмотрим на женщину! Она меньше, стройнее мужчины, у нее плечи уже, а бедра шире. А ее грудь не похожа на маленькие бутончики, как у мужчины, она более выпуклая. В низу живота у женщины есть волосики, как и у мужчины, но нет ни яичек, ни члена.</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image24-0.jpg" alt="Анатомия человека. Голая женщина">
</center>

<p>Для того, чтобы увидеть ее половые органы, мы должны заглянуть в ее живот, как будто смотрим в часовой механизм. И там мы увидим два шарика, названные яичниками, откуда идут две трубы (два канала), толще и короче, чем у мужчин. Эти трубы входят в мешочек с толстыми стенками, напоминающий по форме грушу, внутри полую, это &mdash; матка. Мешочек этот сужается книзу и переходит в канал, названный влагалищем. Отверстие влагалища находится между внутренними сторонами бедер женщины. Если рассмотреть эти яичники под микроскопом, то можно увидеть малюсенькие точки, похожие на булавочные головки.</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image023.jpg" alt="Анатомия человека. Женские половые органы (рисунок). Матка, влагалище, яичник, мочевой пузырь, прямая толстая кишка. Вид анфас и в профиль. Яйцеклетка">
</center>

<p>Это и есть зародышевая клетка (яйцеклетка). Эти клетки зреют в организме женщины, как фрукты. И каждый месяц созревшая клетка выходит из яичника, опускается ниже в маленькую трубу и доходит до матки. Когда один из маленьких сперматозоидов встречается с одной из зародышевых клеток, они сливаются, чтобы появился на свет ребенок! Посмотрите внимательно на этот рисунок!</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image022.jpg" alt="Схема оплодотворения. Сперматозоид и яйцеклетка">
</center>

<p>Много сперматозоидов вошло во влагалище, один из них подошел совсем близко к созревшей яйцеклетке, которая находится в маленькой трубе. Сперматозоид приклеивается к клетке, а затем, соединяясь и перемешиваясь, они образуют шарик, называемый плодом. Плод делится сначала на две части, потом на четыре, затем на восемь и так далее. Но эти частички остаются сцепленными друг с другом. Этот шарик стал похож на ягоду малины или ежевики. Плод растет все больше и больше, перемещается в трубе и двигается к матке. Вот так папа и мама создали будущего мальчика или очень симпатичную девочку!</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image021.jpg" alt="Положение зародыша в женском теле (в матке)">
</center>
<p>&mdash; А дальше что?</p>
<p>&mdash; Плод прикрепляется к стенке матки и больше уже не двигается, мамина кровь циркулирует через стенку матки. И эта кровь приносит все необходимое для развития и роста плода. Он увеличивается постоянно, меняя форму, и потихоньку приобретает вид маленького человеческого существа. Хорошо и тепло ему в этом большом мешочке, где он растет и развивается. Через девять месяцев после встречи сперматозоида с яйцеклеткой ребенок готов появиться на свет.</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image018.jpg" alt="Беременная женщина лежит, мужчина ухаживает и заботиться о ней.">
</center>

<p>Это время, все девять месяцев, для мамы &mdash; время беременности. В первые четыре месяца формируется ребенок. Но он еще очень маленький. Если будущая мама посмотрит на себя в зеркало, то не заметит никаких изменений. Она еще сохраняет свою &laquo;форму&raquo;. К пятому месяцу ребенок становится больше, и матка, которая очень эластична, потихоньку растягивается. В этот период можно заметить небольшое округление животика.</p>
<p>В конце шестого месяца ребенок весит уже около килограмма и достигает длины от 20 до 30 сантиметров. Мамин живот становится больше и круглее. Она его скрывает более свободной и удобной одеждой. Ребенок, чтоб занимать как можно меньше места, сворачивается клубочком в матке. Совсем как спящий котенок! Он частенько двигается, но, как правило, лежит головкой вниз. Еще 2,5&mdash;3 месяца, и период беременности тети Николь подойдет к концу. Ребенок должен родиться, и надо выбрать имя. Но Николь не может это сделать, ведь она еще не знает, родится ли маленький мальчик или девочка!</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image019.jpg" alt="Развитие зародыша в женском теле">
</center>

<p>Тем не менее, дети, знайте, что все девять месяцев у ребенка есть пол! Это зависит от сперматозоида, который встретил яйцеклетку: часть из них может сделать мальчика, а другая часть &mdash; девочку. Мальчик или девочка, но ребенок не может больше оставаться в матке. Ему там слишком тесно, и матка не может больше растягиваться, наоборот, она готова к сокращению, для того чтобы вытолкнуть мальчика наружу. Теперь Николь это почувствует и поймет, что рождение ребенка начинается. К этому дню она долго готовится. Дядя Жак возьмет чемоданчик, куда Николь сложила детские вещички, и они вместе отправятся в родильный дом. Там медсестра, которую называют акушеркой, встретит тетю Николь и будет с ней все время до тех пор, пока малыш не появится на свет.</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image020.jpg" alt="Мужчина помогает беременной женщине добраться до больницы, перинатального центра">
</center>

<p>Николь совсем не боится родов. Вот уже несколько месяцев она ходит на специальные занятия для будущих мам с врачом и акушеркой. Они рассказывают, показывают на рисунках, как проходят роды, и объясняют, что нужно, чтобы все прошло хорошо. Тетя Николь поняла, что для успешных родов нужно быть в хорошей форме. Сначала она научилась очень глубоко и ровно дышать, чтобы кислород проникал в организм. Еще она занималась специальной гимнастикой: это облегченные упражнения, но они делают тело более гибким.</p>

<center><img class="leftimg" width="34%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image017.jpg" alt="Врач объясняет паре за развитие зародыша, учит как дышать во время родов, как и чем питаться во время беременности">
