<p>&mdash; Сначала матка начинает потихоньку раскрываться, затем все шире и шире, быстрее и быстрее. Ребенок продвигается к выходу, но так как открытие матки происходит медленно, он продвигается тоже потихоньку. Тогда мама, чтобы по мочь малышу, подталкивает его с каждой схваткой. Так как она научилась правильно и спокойно дышать, она не задыхается. Малыш продвигается, продвигается, и, наконец, его головка показывается в отверстии влагалища, между ногами матери. Это отверстие растягивается, потому что кожа очень эластичная. Акушерка или врач помогает маленькому человечку выйти на свет. Головка показалась, и&hellip; Вот человек и родился!</p>

<p>Но веревочка, связывавшая малыша с мамой все девять месяцев, через которую он получал необходимую пищу, крепко держит их вместе, и врач обрезает ее. Он делает это быстро и умело, не причиняя боли ни маме, ни ребенку. У малыша только остается маленький шрам, который есть у нас всех,&mdash; это пупок!</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image015.jpg" alt="После родов акушерка передает младенца уставшей, но счастливой матери, счастливый отец рядом">
</center>
<p>Вдыхая воздух в первый раз в жизни, малыш начинает кричать. Вот так он вошел в жизнь. Акушерка показывает его маме, очень усталой, но счастливой. Все закончилось. Теперь мама может заснуть рядом с малышом&hellip;</p>
<p>Ребенок еще очень маленький и незащищенный. В этом взрослом мире он не может еще сам разобраться, он не понимает, что ему холодно. Его пеленают, укладывают в колыбельку.</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image014.jpg" alt="Пара заботится о младенце: кормит, взвешивает, купает, меняет подгузники">
