<p>Птенчик растет внутри яйца. Когда клюв его становится достаточно крепким, он разбивает скорлупу и выходит на белый свет. Он уже такой взрослый, что может бегать и искать зернышки себе на завтрак и на обед.</p>
<p>&mdash; Кстати, о зернышках. А как все это происходит у растений? Откуда берутся новые растения?</p>
<p>&mdash; Большая часть сразу и мужского и женского пола: у них есть и мужские и женские органы. Каждое растение &mdash; и самец и самка одновременно.</p>
<p>У некоторых, например у кукурузы, на одном стебле растут мужские и женские цветы.</p>
<p>У других, например у финиковой пальмы, бывают мужские и женские саженцы. Но таких растений совсем мало.</p>
<p>Возьмем для примера вот этот цветок. Если мы его разрежем вдоль, то увидим, что он, как и часовой механизм, состоит из множества крохотных частичек. Вот это тычинки, а вот это пестик. В пестике есть много-много точек, похожих на булавочные головки &mdash; совсем как в яичнике у женщины; это яйцеклетки. А желтая пудра, которая покрывает тычинки, называется пыльца.</p>

