<p>Посмотрите на часы: стрелки перемещаются медленно по циферблату, но для того чтобы увидеть многочисленные колесики, крутящиеся внутри, надо открыть крышку часов.</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image000.jpg" alt="Разобранные механические часы">
</center>

<p>Чтобы описать вам, как работают <b>половые органы</b>, нужно представить себе человека как бы изнутри. В часах есть крохотные частички, разглядеть которые можно только через лупу. Самые маленькие частички человеческого организма видны только под микроскопом (это такая лупа, только очень мощная). Эти частички строят удивительный, сложный, замечательный мир.</p>

<p>А сейчас давайте сравним мужчин и женщин, мальчиков и девочек. Мужчина чаще всего выше женщины, у него шире плечи. Когда он раздет, можно увидеть волосы в низу живота.</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image025.jpg" alt="Анатомия человека. Голый мужчина в профиль и анфас, рисунок">
