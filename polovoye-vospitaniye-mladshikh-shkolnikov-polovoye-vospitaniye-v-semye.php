<p>&mdash; Это все для ребеночка? &mdash; спросила Софи.</p>
<p>&mdash; Да, это для малыша тети Николь,&mdash; ответила мама.</p>
<p>Тьерри удивился:</p>
<p>&mdash; Но ведь его нет, этого ребенка, где же он?</p>
<p>Мама подвела близнецов к улыбающейся Николь и сказала:</p>
<p>&mdash; Посмотрите, как поправилась тетя за последнее время. Это потому, что малыш там, в животе. Но он очень скоро появится на свет.</p>

<center><img class="leftimg" width="50%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
