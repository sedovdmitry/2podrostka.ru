<p>Наконец, как и все спортсмены, она не ест ничего из того, что могло бы повредить ее здоровью и здоровью малыша. Надо избегать разных приправ и соусов, чтобы не прибавить в весе. Она не пьет вина и кофе, не курит. Ведь вино, кофе и табак очень действуют на нервную систему! Будущая мама должна быть спокойной и мужественной, ведь роды &mdash; процесс болезненный и порой довольно долгий: несколько часов.</p>
<p>&mdash; А как ребенок рождается?</p>
<center><img class="leftimg" width="34%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image016.jpg" alt="Как происходят роды">
