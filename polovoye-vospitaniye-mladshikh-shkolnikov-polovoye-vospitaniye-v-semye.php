<p>Одним словом, они больше никогда не расставались, решили всегда-всегда быть вместе. И поженились, поселились в маленьком домике. Жак помогал Николь все устроить, создать уют. Вскоре молодожены стали подумывать о ребенке.</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image027.jpg" alt="Мужчина делает ремонт, женщина играет с младенцем">
</center>


<p>Ну, а как получается ребенок, я вам сейчас объясню. Слушайте, дети, меня внимательно.</p>
<p>Все дело в том, что детей делают папа и мама. И для этого служат некоторые органы тела, <b>их называют половыми</b>. Они, так же как и сердце, легкие, мозг, работают постоянно, и работа их не видна глазу.</p>

<center><img width="100%" height="auto" sizes="(min-width: 40em) 80vw, 100vw"
		src="ai-cache/768/detyam/entsiklopediya-seksualnoy-zhizni-fiziologiya-i-psikhologiya-detyam-7-9-let-image026.jpg" alt="Мальчик и девочка смотрят в микроскоп">
