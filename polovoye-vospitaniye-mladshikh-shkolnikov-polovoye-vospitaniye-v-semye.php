<p>В перерыве между игрой в мяч ребята серьезно что-то обсуждают. Они задают друг другу вопросы, так как некоторые вещи им кажутся еще неясными. Они говорят о родителях. А родители делают то же самое, что и мы сегодня,&mdash; они объясняют как можно проще и понятнее, как же дети появляются на свет.</p>
<p>Вот эти мальчик и девочка старше вас. Они больше похожи на вашего четырнадцатилетнего брата Франсуа и 12-летнюю сестру Мартин. Девочка любуется на себя в зеркало. Она стала высокой и стройной, у нее развивается грудь, она себе очень нравится. Под мышками и в низу животика появились первые волосики. Теперь она так похожа на маму! У мальчика тоже появились на теле волосы. Голос стал ниже, почти как у папы. Он очень гордится своими широкими плечами и узкими бедрами.</p>

